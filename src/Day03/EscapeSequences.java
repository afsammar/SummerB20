package Day03;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone \nMy name is Ammar");
        System.out.println("\tI am in GA");
        System.out.println("\n\n\n\t\tI love programming");
        System.out.println("\\\\");
        System.out.println("/");
        System.out.println("My name is \"Ammar\"");




    }
}
